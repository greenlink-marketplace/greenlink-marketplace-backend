from django.urls import path
from marketplace.views import (
    ConsumerRegistrationView,
    ProductListView,
    ProductSearchView,
    ConsumerSavedProductListView,
    ConsumerSavedProductCreateView,
    ConsumerSavedProductDestroyView,
    ProductRetrieveView,
    ProductCreateView,
    ConsumerRetrieveView,
    CouponGenerationView,
    CouponListView,
    RelatedProductsView,
)

urlpatterns = [
    path("register/consumer/",
         ConsumerRegistrationView.as_view(),
         name="register-consumer"),
    path('products/list/', 
         ProductListView.as_view(), 
         name='product-list'),
    path('products/search/', 
         ProductSearchView.as_view(), 
         name='product-search'),
    path('products/detail/<int:pk>/',
         ProductRetrieveView.as_view(),
         name='product-detail'),
    path('products/create/',
         ProductCreateView.as_view(),
         name='product-create'),
    path('products/related/<int:pk>/',
        RelatedProductsView.as_view(),
        name='product-related-list'
    ),
    path('saved-products/list/',
         ConsumerSavedProductListView.as_view(),
         name='saved-products-list'),
    path('saved-products/add/',
         ConsumerSavedProductCreateView.as_view(),
         name='saved-product-add'),
    path('saved-products/delete/<int:product_id>/',
         ConsumerSavedProductDestroyView.as_view(),
         name='saved-product-delete'),
    path('consumer/me/',
         ConsumerRetrieveView.as_view(),
         name='consumer-me'),
    path('coupon/generate/', 
         CouponGenerationView.as_view(), 
         name='coupon-generate'),
    path('coupon/list/', 
         CouponListView.as_view(), 
         name='coupon-list'),
]
